<h1>ABOUT BVP-CSI</h1>
<p>
   BVP-CSI is a new Student chapter of BVCOE - New Delhi and a sub-chapter of
   the CSI Community. Here at BVP-CSI we believe that there are different ways
   of solving a problem. So if you fail at any stage, get back and find another
   way. We aim to enhance the productivity and capabilities of new IT engineers.
   Everyone has ideas, but being truly creative and innovative is difficult.
   At CSI, we encourage curiosity among its members. We help students identify
   their field of interest and then guide and help them excel in their area of
   interest. We believe in excellence and perfection in all that we do, to
   ensure the best performance out of software engineers.
</p>
<h2>Benefits of BVP-CSI</h2>
<ul>
    <li>Access to all the resources of CSI (Including online portal)</li>
    <li>You can attend all the events, sessions and workshops organized by BVP-CSI</li>
    <li>Build projects and participate in research</li>
    <li>Learn what you want to learn</li>
</ul>

<h1>About CSI</h1>
<p>
   <strong>CSI - Computer Society of India</strong> is the first and the largest
   body of computer professionals in India. Formed in 1965 by a few computer
   enthusiasts, it has now grown to be the national body representing IT
   professionals. CSI has been instrumental in guiding the Indian IT industry
   down the right path since its formative years. Today, CSI has 72 chapters all
   over India, 511 student branches, and more than 100000 members including
   India's most famous IT industry leaders, brilliant scientists and dedicated
   academicians.
</p>
<p>
   The mission of CSI is to facilitate research, knowledge sharing, learning and
   career enhancement for all categories of IT professionals, while
   simultaneously inspiring and nurturing new entrants into the industry and
   helping them to be integrated into the IT community.
</p>

<h3>To know more about CSI you can visit : <a href = "http://www.csi-india.org">Official Website</a> or Wikipedia</h3>
